class animal:
	def __init__(animal, location, race, name, health, strenght, agility, weapon, fur, alive):
		animal.location = location
		animal.race = race
		animal.name = name
		animal.health = health
		animal.strenght = strenght
		animal.agility = agility
		animal.weapon = weapon
		animal.fur = fur
		animal.alive = alive